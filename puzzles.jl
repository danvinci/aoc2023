# ╔═╡ eff84079-19fe-48d9-93fd-5bffca68e8af
function solve_p4_part2(input)

	_mod_input = Dict()
	
	# let's keep track of how many copies of each card I have
	for id in collect(keys(input))

		_card_details = deepcopy(input[id])
		setindex!(_card_details, [1], :copies) # modify value
		setindex!(_mod_input, _card_details, id) # write modified dict
		
	end

	# check for matches, count them, and increase number of copies for each row 
	for id in sort!(collect(keys(_mod_input))) # 1, 2, 3, ...

		# iterate over number of copies
		for run in 1:_mod_input[id][:copies][1]
			
			_matches_found = 0
			
			# check for matches and count them
			for w in _mod_input[id][:win]
				for o in _mod_input[id][:oth]
					if w == o
						_matches_found += 1
					end
				end
			end
	
			# alter copies numbers for following rows
			if _matches_found >= 1
				_affected_rows = [j for j in (id+1):(id+_matches_found)]
				
				for _r in _affected_rows
					_mod_input[_r][:copies][1] += 1 # up by 1 copies for affected rows
				end
			end
		end
	end


	_mod_input
	
end

