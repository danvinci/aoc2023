# ╔═╡ a0036952-a1e7-46ae-82b0-d01dd1c63162
function prep_p4_input(input)

	_rows = split(input, "\n")

	_dict = Dict()

	for r in _rows

		# split card id from rest of numbers
		_card_txt, _all_nums_txt = split(r, ":") 

		# get card id
		_card_id = parse(Int, SubString(_card_txt, findfirst(r"[0-9]+", _card_txt)))

		# extract text of winning and other numbers
		_win_num_txt, _oth_num_txt = split(_all_nums_txt, "|")

		# extract isolated numbers
		_win_num_int = SubString.(_win_num_txt, findall(r"[0-9]+", _win_num_txt))
		_oth_num_int = SubString.(_oth_num_txt, findall(r"[0-9]+", _oth_num_txt))
			
		setindex!(_dict, Dict( 	:win => parse.(Int, _win_num_int), 
								:oth => parse.(Int, _oth_num_int)), _card_id)

		

	end

	_dict
	
end

