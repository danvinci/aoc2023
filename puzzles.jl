# ╔═╡ cf470314-94a2-40fd-8c06-c94d9d8237b7
function solve_p4(input)

	_card_scores = []

	for id in collect(keys(input))

		_c_score = 0
		_matches_found = 0

		for w in input[id][:win]
			for o in input[id][:oth]

				if w == o
					_matches_found += 1
					if _matches_found > 1
						_c_score *= 2 # double on other matches
					else
						_c_score = 1 # +1 points on first match
					end
				end
			end
		end

		push!(_card_scores, _c_score)
	end

	sum(_card_scores)
end

